    """–ü–æ–ª—É—á–∞–µ—Ç –¥–∞–Ω–Ω—ã–µ –æ —Ä–∞–±–æ—Ç–æ–¥–∞—Ç–µ–ª–µ –∏ –µ–≥–æ –≤–∞–∫–∞–Ω—Å–∏—è—Ö –ø–æ ID."""

